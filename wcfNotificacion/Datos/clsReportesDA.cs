        public bool Vencidos_Pendiente(int iIdConexion, string cOperacion,Int32 IdOficina,Int32 TipoReporte,string TipoDocumento,string FechaIni,string FechaFin,ref string sMensajeError, ref DataSet DSetTmp)
        {
            ClassSPExec ObjSPExec = new ClassSPExec(iIdConexion, "Notificacion.PR_Reportes", cOperacion);
            ObjSPExec.p_RemplazarCeroPorDBNull = false;
            if (!ObjSPExec.p_bEstadoOK)
            {
                sMensajeError = ObjSPExec.ObtenerPilaMensajesError();
            }
            else
            {
                bool bAsignacionOK = true;
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@s_iIdConexion", iIdConexion);
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@s_cOperacion", cOperacion);
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@Regional", IdOficina);
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@IdTipoReporte", TipoReporte);
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@IdDocumento", TipoDocumento);
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@FechaIni", FechaIni);
                bAsignacionOK = bAsignacionOK && ObjSPExec.AsignarValorParametro("@FechaFin", FechaFin);
                if (bAsignacionOK)
                {
                    if (!ObjSPExec.EjecutarProcedimientoQry())
                    {
                        sMensajeError = ObjSPExec.ObtenerPilaMensajesError();
                    }
                    else
                    {
                        DSetTmp = ObjSPExec.p_DataSetResultado;
                    }
                }
            }
            return (ObjSPExec.p_bEstadoOK);
        }

